#include "libft.h"

void	ft_putnbr(int nb)
{
	long int li;

	li = nb;
	if (li < 0)
	{
		ft_putchar('-');
		li *= -1;
	}
	if ((li / 10) > 0)
		ft_putnbr(li / 10);
	ft_putchar(li % 10 + 48);
}
