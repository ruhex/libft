int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (((--n != 0) && (*s1 == *s2++)))
		if (*s1++ == '\0')
			return (0);
	return (*(const unsigned char *)s1 - *(const unsigned char *)(s2 - 1));
}
