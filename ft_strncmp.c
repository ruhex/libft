int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	if (n == 0)
		return (0);
	while ((--n != 0) && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return (s1 - s2);
}
