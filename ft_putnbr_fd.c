#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
	long int li;

	li = n;
	if (li < 0)
	{
		ft_putchar_fd('-', fd);
		li *= -1;
	}
	if ((li / 10) > 0)
		ft_putnbr(li / 10);
	ft_putchar_fd(li % 10 + 48);
}
