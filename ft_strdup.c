#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int 	i;
	char	*tmp;


	tmp = s1;
	i = 1;
	while (*s1++)
		i++;
	s1 = tmp;
	tmp = ft_strnew(i);

	return (ft_memcpy(tmp, s1, i));

}
